import UIKit

public extension UIColor {
    public class func orlandoGreenColor(alpha: CGFloat = 1.0) -> UIColor {
        return UIColor(red: 0.5294117647, green: 0.7333333333, blue: 0.3960784314, alpha: alpha)
    }
}